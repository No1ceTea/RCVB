        Les règles y sont aménagées (ex : pas besoin de poser le ballon pour marquer, franchir la ligne suffit) Ils ont démontré qu’à travers le sport, et plus précisément le Rugby, 40% des patientes qui se sont mis au sport n’ont pas eu de récidives sur leur cancer. Bien que leur corps soit fatigué par des traitements très lourds, le fait de faire du sport et de s’inscrire dans une démarche collective, permet de générer de l’Adénosine Tri-Phosphate, et de freiner la maladie.
