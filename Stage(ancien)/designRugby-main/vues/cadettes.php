        <p class="card-text">Joueuses nées entre 2003 et 2005. <br> La section féminine et plus particulièrement l’équipe cadette du R.C. Val de Bièvre est née en 2010 pour répondre à la demande de jeunes filles de pouvoir continuer à jouer au rugby au sein du R.C. Val de Bièvre après leurs années minimes. La politique du club étant d’ouvrir la pratique du rugby pour tous, nous nous sommes engagés dans le développement du rugby féminin.
 Avec une vingtaine d’adhérentes,  depuis trois saisons cette équipe est devenue compétitive et nous avons remporté dès la saison 2011/2012 les places de troisième d’Ile de France et de septième du secteur nord-est  ainsi que le tournoi régional de Limay.
Le club a doté cette équipe cadettes de 3 entraineurs et d’une dirigeante afin de permettre aux joueuses confirmées de continuer à progresser et aux joueuses débutantes de se mettre à niveau. L’engagement, la solidarité et la convivialité entre les joueuses font de ce groupe une équipe extrêmement soudée ce qui leur permet de se surpasser à chaque rencontre.
Afin de pérenniser la section féminine,  les joueuses et le staff recrutent tout au long de l’année de nouvelles  joueuses. L’engagement du RCVB  dans le développement du rugby féminin  nous a conduit à devenir club support  dans le Val de Marne afin de permettre aux jeunes filles des autres clubs  qui n’ont pas d’équipe cadettes de pouvoir continuer à pratiquer notre sport.</p>
