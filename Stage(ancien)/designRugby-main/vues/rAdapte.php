        <p class="card-text">Cette rubrique regroupe les jeunes de 8 à 16 ans atteints d'un handicap mental.<br><br>
          Une fois de plus, le RCVB colle à sa devise : « Notre Rugby se conjugue à la première personne du pluriel ».
C’est en octobre 2019 que la catégorie de Rugby adapté a été créée afin de favoriser l’inclusion par le rugby.
Elle est actuellement composée de 6 enfants âgés de 9 à 16 ans souffrant d’un handicap mental ou psychique.<br><br> Encadrés par Adrien, Alice, Doudou, Sarah et Nicolas et avec la participation de certains parents, les jeunes commencent toujours l’entrainement par un parcours moteur suivi de différents ateliers (passe, passage au sol, etc...). La fin de séance est ritualisée autour d’un cri de rassemblement : « RCVB CHANKA » et d’une 3ème mi-temps, moment très attendu par les enfants et les parents !<br>
